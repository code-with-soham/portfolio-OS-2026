import { useState, useRef } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { ArrowDownloadRegular, DismissRegular } from '@fluentui/react-icons';

const FORMATS = [
  { id: 'json', label: 'JSON', icon: '📦', description: 'Download architecture knowledge base' },
  { id: 'markdown', label: 'Markdown', icon: '📝', description: 'Formatted architecture report' },
  { id: 'png', label: 'PNG', icon: '🖼', description: 'Screenshot of current view' }
];

async function exportJSON() {
  // Dynamically import all JSON files
  const modules = import.meta.glob('../../../ai/knowledge/architecture/*.json', { eager: true });
  const data = {};
  for (const [path, mod] of Object.entries(modules)) {
    const name = path.split('/').pop().replace('.json', '');
    data[name] = mod.default || mod;
  }

  const blob = new Blob([JSON.stringify(data, null, 2)], { type: 'application/json' });
  downloadBlob(blob, 'portfolio-os-architecture.json');
}

